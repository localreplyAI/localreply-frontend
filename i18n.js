// Moteur i18n
// ══════════════════════════════════════════════

const LRI18n = {
  _lang: null,

  // Langue courante
  get lang() {
    if (!this._lang) {
      this._lang = localStorage.getItem('lr_lang') || 'fr';
    }
    return this._lang;
  },

  // Changer la langue
  setLang(lang) {
    if (!LR_TRANSLATIONS[lang]) return;
    this._lang = lang;
    localStorage.setItem('lr_lang', lang);
    this.applyAll();
    // Mettre à jour les sélecteurs de langue
    document.querySelectorAll('.lr-lang-selector').forEach(sel => sel.value = lang);
    document.querySelectorAll('.lr-lang-flag').forEach(el => el.textContent = this.flag(lang));
  },

  // Traduire une clé (ex: 'nav.home' ou 'common.loading')
  t(key) {
    const parts = key.split('.');
    let obj = LR_TRANSLATIONS[this.lang];
    for (const p of parts) {
      if (!obj) return key;
      obj = obj[p];
    }
    return obj || LR_TRANSLATIONS['fr'][parts[0]]?.[parts[1]] || key;
  },

  // Drapeau
  flag(lang) {
    const flags = { fr:'🇫🇷', en:'🇬🇧', de:'🇩🇪', it:'🇮🇹', es:'🇪🇸', pt:'🇵🇹' };
    return flags[lang] || '🌐';
  },

  // Nom de la langue
  langName(lang) {
    const names = { fr:'Français', en:'English', de:'Deutsch', it:'Italiano', es:'Español', pt:'Português' };
    return names[lang] || lang;
  },

  // Appliquer les traductions sur tous les éléments [data-i18n]
  applyAll() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const attr = el.getAttribute('data-i18n-attr');
      const val = this.t(key);
      if (attr) el.setAttribute(attr, val);
      else el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = this.t(el.getAttribute('data-i18n-html'));
    });
  },

  // Créer un sélecteur de langue HTML
  createSelector(className = '') {
    const langs = ['fr', 'en', 'de', 'it', 'es', 'pt'];
    const sel = document.createElement('select');
    sel.className = 'lr-lang-selector ' + className;
    sel.value = this.lang;
    sel.onchange = (e) => this.setLang(e.target.value);
    langs.forEach(l => {
      const opt = document.createElement('option');
      opt.value = l;
      opt.textContent = this.flag(l) + ' ' + this.langName(l);
      if (l === this.lang) opt.selected = true;
      sel.appendChild(opt);
    });
    return sel;
  },

  // Init — appeler au DOMContentLoaded
  init() {
    this.applyAll();
    // Détecter la langue du navigateur si pas encore définie
    if (!localStorage.getItem('lr_lang')) {
      const browserLang = navigator.language?.slice(0,2);
      if (LR_TRANSLATIONS[browserLang]) {
        this._lang = browserLang;
        localStorage.setItem('lr_lang', browserLang);
        this.applyAll();
      }
    }
  }
};

// Raccourci global
const t = (key) => LRI18n.t(key);

// Auto-init
document.addEventListener('DOMContentLoaded', () => LRI18n.init());
